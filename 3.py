
# Question 3: How many tiles whose length
#  and breadth are 13 cm and 7 cm
#  respectively are needed to cover a 
# rectangular region whose length and breadth 
# are 520 cm and 140 cm?
tl = 13
tb = 7
ta = tl * tb
rl = 520
rb = 140
ra = rl * rb
tiles = ra / ta
print("3. tiles needed to cover  =", tiles)